          <h3>
            National Seminar on Children with Special Needs: Thinking Outside
            the Box.
          </h3>
          <p>
            The National Seminar on Children with Special Needs: Thinking
            Outside the Box was organized recently at L.L.R.M. College of
            Education Dhudike. It was inaugurated by Dr. S.S. Bari, Registrar ,
            Panjab University, Chandigarh accompanied by Dr. Raj Gupta, HOD,
            Deptt. of Edu, P.U., Chd, Dr. Sudesh Gakhar (Ex. H.O.D. Dept. of
            Education), Dr. Ravinder kaur (Dean faculty of Education), Sh. H.L.
            Shrama (Managing Director), Dr. Sawaranjit Singh Brar (President),
            Dr. Tripta Sharma (Principal L.L.R.M. College of Education), Dr.
            Sushma Sharma (Principal), Dr. H.S. Brar (Principal), Dr. N.K.
            Chaudhary (Principal), Dr. Geeta Sharma (Principal), Dr. Ranjita
            Bhalla (Principal), Dr. Veena Sodhi (Principal), Dr. D.S. Pannu
            (Principal), Dr. J.S. Brar (Principal).
          </p>
          <p>
            Welcoming the guests and other delegates, Dr. Tripta sharma said
            that 40 to 80 million persons are living with disability in India.
            She laid emphasis on the need for a holistic approach on the
            inclusive education. There is a need to change the education system
            to make it accessible to all children and prepare the society the
            parents, friends and employers to provide support to the disabled
            children. We must teach parents how to become an effective advocate
            for their child. A primary goal of such advocacy is the skill to
            empower parents to be more effective and knowledgeable about
            provisions and schemes. These informed parents will be better able
            to make rational decisions for their child.
          </p>
          <p>
            In the inaugural address, Prof S.S. Bari shared his views about
            special education. He said that human being had interfered with the
            Mother Nature. Use of banned drugs during pregnancy effects the
            development of child. Exclusive use of pesticides is polluting the
            environment. These pesticides became an integral part of our food
            chain and are responsible for deformities and disabilities. He
            further stressed on the role of teacher. He said that this problem
            can only be tackled by teachers. The teachers are the only persons
            who can transform these students to the useful person to the
            society.
          </p>
          <p>
            The keynote of the seminar was presented by Dr. Raj Gupta, HOD
            Deptt. of Edu., P.U. Chd. She said that the need is to think
            unconventional for education and general upkeep of these children. A
            collaborative learning utilizing peer learning approach should be
            initiated. It will lead to the acceptance of special child in case
            of inclusive situations. Parents should be partners in the process
            of education. Schools should keep computers and printers which can
            print in Braille. There is a need to change evaluation modes and
            need based curricula should be developed. Counseling facilities may
            be provided for parents, effected children, peers, and buddies and
            teachers dealing with children with special needs. The Schools and
            other organizations can hold joint activities of regular and special
            children. This will contribute towards awareness and finally
            mainstreaming.
          </p>
          <p>
            Dr. Kamlesh Rai presented the paper on behavioral planning and
            management for children with special needs. He discussed in detail
            about the cognitive understanding of the terms used in special
            education. He emphasized upon the two types of teaching strategies
            for planning and managing behavior of special children. These are
            general behavior strategies and cognitive behavior strategies. He
            further stressed on the role of regular class teacher. He said that
            the teacher should be conversant and trained in behavior
            modification techniques so that he can easily manage classroom
            behavior of his students. He shared some behavior techniques with
            would be teachers.
          </p>
          <p>
            Dr. C.S. Ravinderan in his paper “problems &strategies for visually
            impaired children” gave tips to be followed in schools i.e.
            orientation of the whole school; encourage the peers to help the
            visually impaired; follow the visually impaired to participate in
            all school activities. He explained some skills that a visually
            impaired should learn. The skills of orientation and mobility, daily
            living skills, social skills, auditory discrimination skills etc.
            The teacher should present the study material in clear way. At the
            end, he said that we should promote inclusive education and the main
            responsibility for the success of inclusive education is on the
            teachers.
          </p>
          <p>
            The first technical session was concluded by Dr. H.S. Brar. He
            summed up the session by adding one more dimension to the categories
            of special children i.e. transgender children. He said that we must
            include these children in special education because they need our
            support and special care. The transgender children have social
            stigma in their mind and we should come forward to remove these
            social barriers for their growth. The seminar was attended by eighty
            six delegates from different states attended the seminar and thirty
            four delegates presented their paper on these different sub themes.
          </p>
          <p>
            The second technical session was divided into five sub themes i.e.
            (i) diagnosing and addressing the needs of students with emotional
            disabilities, (ii) understanding individuals with learning
            disabilities, (iii) designing creative behavioral plans to deal with
            aggressive behaviors, (iv) recognition and intervention strategies
            for students who are at risk, (v) behavioral problems and remedies
            of children with special needs. The second technical session was
            concluded by Dr. Ravinder Kaur. She said that everyone is special in
            his/her field. The need is to note your self, be what you are and
            give what you can. The valedictory session was presided over by Dr.
            Sudesh Gakhar and Dr. Ravinder Kaur. Certificates were distributed
            to the delegates by Dr. Sudesh Gakhar. College principal Dr. Tripta
            Sharma thanked the delegates for their participation and making the
            seminar a great success.
          </p>
